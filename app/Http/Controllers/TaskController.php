    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required|max:255',
            'description' => 'nullable'
        ]);

        Task::create([
            'title' => $request->title,
            'description' => $request->description,
            'user_id' => 1
        ]);

        return redirect()->route('tasks.index');
    }

